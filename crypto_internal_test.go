func _byteArray(input string) []byte {
	res, _ := hex.DecodeString(input)
	return res
}

func _32byteArray(input string) [32]byte {
	bytes, _ := hex.DecodeString(input)
	var res [32]byte
	copy(res[:], bytes)
	return res
}

func TestOSToIP(t *testing.T) {
	tests := []struct {
		name   string
		input  []byte
		output *big.Int
	}{
		{
			name:   "Nil",
			input:  nil,
			output: _bigInt("0"),
		},
		{
			name:   "Empty",
			input:  []byte{},
			output: _bigInt("0"),
		},
		{
			name:   "TestA",
			input:  []byte("a"),
			output: _bigInt("97"),
		},
		{
			name:   "TestAB",
			input:  []byte("ab"),
			output: _bigInt("24930"),
		},
	}

	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			output := osToIP(test.input)
			if test.output.Cmp(output) != 0 {
				t.Errorf("Unexpected output: expected %v, received %v", test.output, output)
			}
		})
	}
}

